num1 = 100
num2 = 50

#Integer Addition
addition = num1 + num2
print("Additional value is:", addition)

#Integer Subtraction
subtraction = num1 - num2
print("Subtraction value is:", subtraction)

#Integer Multiplication
multiplication = num1 * num2
print("Multiplication value is:", multiplication)

#Integer Division
division_float = num1 / num2                      #using single / will give results in float type i.e 2.0
print("Division value is:", division_float)

division_int = num1 // num2
print("Division value is:", division_int)         #using double // will give results in int type i.e 2

#Modulus (Remainder)
remainder = num1 % num2
print("Remainder value is:", remainder)

#Absolute value
absolute_val = abs(-50)     #used to ensure that a number is non-negative
print(absolute_val)

