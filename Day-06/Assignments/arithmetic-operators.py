#Create two variables a and b with numeric values.
#Calculate the sum, difference, product, and quotient of a and b.
#Print the results.

a = 10              # '=' is assignment operator
b = 20

add = a + b         # '+' addition operator
diff = a - b        # '-' subtraction operator
product = a * b     # '*' multiplication operator
quotient1 = a / b   # '/' Division operator - float output
quotient2 = a // b  # '//' Division operator - int output

print(f"Value of Addition:",add)
print(f"Value of Subtraction:",diff)
print(f"Value of Multiplication:",product)
print(f"Value of Divison1:",quotient1)
print(f"Value of Divison2:",quotient2)